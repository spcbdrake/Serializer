public class Game {
    String name;
    String genre;
    String platform;
    int rating;
    double price;

    public String getName() {
        return name;
    }

    public String getGenre() {
        return genre;
    }

    public String getPlatform() {
        return platform;
    }

    public int getRating() {
        return rating;
    }

    public double getPrice() {
        return price;
    }
}

